function V = V_pcr3bp(x,y, mu)
%==========================================================================
%
% Potential for the planar CR3BP
%
%
% Author: G. Montseny
% Date: November 21, 2025
%
%==========================================================================
V = 0.5.*(x.^2+y.^2)+(1-mu)./sqrt((x+mu).^2+y.^2)+mu./sqrt((x-1+mu).^2+y.^2);