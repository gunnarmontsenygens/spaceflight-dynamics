function [t_p] = calc_tp(c,s,theta,mu)
%==========================================================================
%
%
% Author: G. Montseny
% Date: October 14, 2025
%
%
%==========================================================================

if theta < pi 
    t_p = (sqrt(2)/(3*sqrt(mu)))*(s^(3/2)-(s-c)^(3/2));
else
    t_p = (sqrt(2)/(3*sqrt(mu)))*(s^(3/2)+(s-c)^(3/2));
end
